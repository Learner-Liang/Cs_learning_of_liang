/*在完成了分配任务之后，西部314来到了楼兰古城的西部。相传很久以前这片土地上(比楼兰古城还早)生活着两个部落，一个部落崇拜尖刀(‘V’)，
一个部落崇拜铁锹(‘∧’)，他们分别用V和∧的形状来代表各自部落的图腾。 西部314在楼兰古城的下面发现了一幅巨大的壁画，壁画上被标记出了N个点，
经测量发现这N个点的水平位置和竖直位置是两两不同的。
西部314认为这幅壁画所包含的信息与这N个点的相对位置有关，因此不妨设坐标分别为(1, y1), (2, y2), …, (n, yn), 其中y1...yn是1到n的一个排列。

​ 西部314打算研究这幅壁画中包含着多少个图腾，其中V图腾的定义如下（注意：图腾的形式只和这三个纵坐标的相对大小排列顺序有关）1≤i < j<k≤n且yi>yj, yj < yk;

​ 而崇拜∧的部落的图腾被定义为1≤i < j < k≤n且yi<yj, yj>yk;

​ 西部314想知道，这n个点中两个部落图腾的数目。因此，你需要编写一个程序来求出V的个数和∧的个数。*/
#define _CRT_SECURE_NO_WARNINGS
#include <iostream>
#define lowbit(x) (x)&(-x)
using namespace std;
//以中间的一个点，前面和后面小于和大于他的值
//树状数组可以用来统计前面有几个数字小于当前这位的数字
int query__(int* c, int ind)
{
// 步骤1（代码实现-输入与边界）：先读取题目输入，并明确变量含义与取值范围。
// 目的：让后续每个状态/循环都建立在正确输入之上，避免越界与未初始化。
// 关键代码说明：紧随其后的 cin/scanf 语句负责读入 n、m、权值、边信息等原始数据。
// 步骤2（代码实现-状态定义与初始化）：构建 dp/图/数组/容器等数据结构并设置初值。
// 目的：把“题意”翻译成“可计算状态”，并给转移提供合法起点（如 0、INF、初始答案）。
// 关键代码说明：常见初始化语句（memset、赋初值、清空容器）决定了后续转移是否正确。
// 步骤3（代码实现-核心转移/搜索）：在循环或递归中执行状态更新，逐步逼近最优解/可行解。
// 目的：把局部决策累积为全局答案；若是 DP，本质是“已知子问题 -> 当前问题”的转移。
// 关键代码说明：max/min/松弛/合并等更新语句是核心；它们对应题目中的数学关系或最优性原理。

	int sum = 0;
	
	// 步骤3细化（核心循环解释）：该循环按既定顺序遍历状态/结点。
	// 顺序本身通常携带算法约束（如 0/1 背包倒序遍历、最短路按当前最小距离扩展）。
	while (ind)
	{
		// 步骤3细化（判断条件解释）：这里的 if/while 条件用于筛掉非法状态或无效分支，只保留满足约束的候选。
		// 为什么这么写：条件成立时才更新/扩展，能保证正确性并避免错误转移；本质是把题目约束翻译成程序判定规则。

		sum += c[ind];
		ind -= lowbit(ind);
	}
	

// 步骤4（代码实现-答案输出）：按照题目要求的格式输出最终结果并结束程序。
// 目的：把计算得到的最优值/可行解返回给评测系统，形成完整求解闭环。
// 关键代码说明：cout/printf 的输出对象必须与题意一致（如最大值、最短路、方案数等）。
return sum;
}
void add__(int* c, int ind, int n)
{
	while (ind <= n)
	{
		c[ind]++;
		ind += lowbit(ind);
	}
	return;
}
void init(int* c, int n)
{
	for (int i = 1;i <= n;i++)
		c[i] = 0;
	return;
}
int main15()
{
	//开辟一个数组，去统计出现的数字，改成树状数组的形式，通过这个数组可以求的在当前位之前的数字出现了多少个
	//使用前缀和去维护
	int l[100] = { 0 };//i位置之前有多少个值小于
	int r[100] = { 0 };//i位置之后小于的数量给
	int a[100] = { 0 };
	int c[100] = { 0 };
	int n;
	
	// 步骤1细化（输入语句解释）：这一行把原始数据读入变量，定义了问题规模与基础参数。
	// 若输入不完整或变量含义对应错误，后续所有状态转移都会失真。
	cin >> n;
	for (int i = 1;i <= n;i++)cin >> a[i];
	for (int i = 1;i <= n;i++)
	{
		l[i] = query__(c, a[i]);
		add__(c, a[i], n);
	}
	init(c, n);
	for (int i = n;i ;i--)
	{
		r[i] = query__(c, a[i]);
		add__(c, a[i], n);
	}
	int ans1=0, ans2 = 0;
	for (int i = 1;i<=n;i++)
	{
		
		// 步骤4细化（输出语句解释）：该语句输出最终答案，代表全部状态计算完成。
		//cout << l[i] << " " << r[i] << endl;
		ans1 += l[i] * r[i];
		ans2 += (i - 1 - l[i]) * (n - i - r[i]);
	}
	cout << ans2 << " " << ans1;
	return 0;
}