{
	int n;
	
	// 步骤1细化（输入语句解释）：这一行把原始数据读入变量，定义了问题规模与基础参数。
	// 若输入不完整或变量含义对应错误，后续所有状态转移都会失真。
	cin >> n;
	int head[6005] = { 0 };
	int fa[6005] = { 0 };//父节点存在的判断
	int dp[6005][2] = { 0 };
	edge Edge[6005] = { 0 };
	for (int i = 1;i <= n;i++) scanf("%d", h + i);
	for (int i = 1,a,b;i < n;i++)
	{
		cin >> a >> b;
		add__(Edge,head,b, a);
		fa[a] = 1;
	}
	int root;
	for (int i = 1;i <= n;i++)
	{
		if (fa[i])continue;
		root = i;
		break;
	}
	GetDp_(dp, Edge, head, root);
	
	// 步骤4细化（输出语句解释）：该语句输出最终答案，代表全部状态计算完成。
	cout << max(dp[root][0], dp[root][1]);
	return 0;
}