int dp11[100] = { 0 }, t1[100] = { 0 };
void dps2(edge* edge, int* head, int f, int s)
{
	int p = head[s];
	
	// 步骤3细化（核心循环解释）：该循环按既定顺序遍历状态/结点。
	// 顺序本身通常携带算法约束（如 0/1 背包倒序遍历、最短路按当前最小距离扩展）。
	while (p)
	{
		int to = edge[p].first;
		if (to != f)
		{
			dps2(edge, head, s, to);
			
			// 步骤3细化（判断条件解释）：这里的 if/while 条件用于筛掉非法状态或无效分支，只保留满足约束的候选。
			// 为什么这么写：条件成立时才更新/扩展，能保证正确性并避免错误转移；本质是把题目约束翻译成程序判定规则。
			t1[s] = max(t1[s], t1[to] + edge[p].t);
		}
		p = edge[p].second;
	}
	p = head[s];
	while (p)
	{
		int to = edge[p].first;
		if (to != f)
		{
			dp11[s] += t1[s] - edge[p].t - t1[to] + dp11[to];
		}
		p = edge[p].second;
	}
	return;
}
